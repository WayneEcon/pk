def get_enhanced_distance(country_code: str, energy_product: str = None) -> float:
    """
    获取增强的国家距离数据
    
    Args:
        country_code: ISO 3166-1 alpha-3 国家代码
        energy_product: 能源产品类型（可用于未来的产品特异性距离）
        
    Returns:
        距离（公里）
    """
    # 使用CEPII权威数据
    distance = COMPLETE_COUNTRY_DISTANCES.get(country_code)
    
    if distance is not None:
        return distance
    
    # 如果仍然没有找到，使用区域平均值
    # 这比全球平均值更精确
    regional_averages = {
        # 基于UN地理区域的平均距离
        'africa': 10500,
        'asia': 11500, 
        'europe': 7000,
        'oceania': 16000,
        'south_america': 7500,
        'north_america': 3500
    }
    
    # 简单的区域映射（可以进一步完善）
    country_regions = {
        # 非洲国家样例
        'ETH': 'africa', 'KEN': 'africa', 'TZA': 'africa',
        # 亚洲国家样例  
        'THA': 'asia', 'VNM': 'asia', 'MYS': 'asia',
        # 欧洲国家样例
        'SVK': 'europe', 'SVN': 'europe', 'EST': 'europe',
        # 大洋洲
        'FJI': 'oceania', 'PNG': 'oceania',
        # 南美洲
        'URY': 'south_america', 'PRY': 'south_america',
        # 北美洲/加勒比
        'GTM': 'north_america', 'HND': 'north_america'
    }
    
    region = country_regions.get(country_code, 'asia')  # 默认亚洲
    return regional_averages[region]

